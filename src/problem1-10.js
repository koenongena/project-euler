const isPalindrome = (n) => R.equals(n.toString(), R.reverse(n.toString()));

const findLargestPalindrome = (max) => {
    return R.pipe(
        R.converge(R.xprod, [R.range(1), R.range(1)]),
        R.map(([a, b]) => a * b),
        R.sort(R.descend(R.identity)),
        R.find(isPalindrome),
    )(max);
};

